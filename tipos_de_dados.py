"""
Tipos de dados:

tipo String ou STR:

São dados alfanuméricos que são armazenados como texto, muito ultilizados por exemplo para nomes, e devem ser referenciados entre aspas simples ou duplas, mas sempre sendo igual
ao caractere de inicio
"""
#Ex:
nome = 'Marcelo'
print(nome)

"""
Tipo Int:
São números INTEIROS sem casas decimais pode ser positivos ou negativos

Tipo Float:
São números com casa DECIMAIS negativos ou positivos pontos flutuante
Obs: nas linguagens de programação não é usado virgula para representar as casas decimais.
ao inves disso é usado ponto.
"""
#Ex:
inteiro = 65 #int
flutuante = 65.5 #Float

print(type(inteiro))
print(type(flutuante))

""" 
Tipo Booleano:

Booleano são dados usados para testes lógicos os famosos True e False, verdadeiro e falso.

"""
#Ex:
verdadeiro = True
falso = False

print(type(verdadeiro))
print(type(falso))